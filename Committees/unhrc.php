                <p>
The UNHRC is a highly significant council due to the fact that it is tasked with bringing awareness and protection of human rights by addressing the violations and passing effective resolutions that put forth solutions to various human rights issues around the world as well as providing support and resources to human rights emergencies.Some issues the UNHRC has addressed in the past include child prostitution, adequate housing, safe water, freedom of expression, education as well as many other gravely important issues. </p>
<p>
The promotion and protection of human rights and international respect without discrimination based on race, gender, language or religion are the overall goals of the UNHRC and it works on conducting research and implementing programs targeting civil, cultural, educational, economic and political rights in order to reach these goals. </p>
